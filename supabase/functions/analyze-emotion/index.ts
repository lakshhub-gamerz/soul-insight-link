import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { feeling } = await req.json();

    if (!feeling || typeof feeling !== "string") {
      return new Response(
        JSON.stringify({ error: "Please provide a feeling description" }),
        { status: 400, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const systemPrompt = `You are an empathetic emotional intelligence AI. Analyze the user's emotional state based on their description.

Return a JSON object with exactly these fields:
- emotion: The primary emotion detected (one word, e.g., "Joy", "Anxiety", "Peace", "Sadness", "Frustration", "Excitement", "Fatigue", "Loneliness", "Gratitude", or "Mixed")
- intensity: The intensity level ("Low", "Moderate", "High", or "Very High")
- insight: A brief, compassionate insight about their emotional state (1-2 sentences)
- suggestion: One actionable suggestion to support their wellbeing (1 sentence)

Be warm, understanding, and avoid clinical language. Focus on validation and gentle guidance.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          { role: "user", content: `The user says they are feeling: "${feeling}"` },
        ],
        temperature: 0.7,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limit exceeded, please try again later." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "Service temporarily unavailable." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      throw new Error(`AI API error: ${response.status}`);
    }

    const data = await response.json();
    const content = data.choices?.[0]?.message?.content;

    if (!content) {
      throw new Error("No response from AI");
    }

    // Parse the JSON from the response
    let result;
    try {
      // Try to extract JSON from the response
      const jsonMatch = content.match(/\{[\s\S]*\}/);
      if (jsonMatch) {
        result = JSON.parse(jsonMatch[0]);
      } else {
        throw new Error("No JSON found in response");
      }
    } catch {
      // Fallback response
      result = {
        emotion: "Mixed",
        intensity: "Moderate",
        insight: "Your feelings are valid and worth exploring.",
        suggestion: "Take a moment to breathe deeply and observe your emotions without judgment.",
      };
    }

    console.log("Emotion analysis result:", result);

    return new Response(JSON.stringify(result), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  } catch (error) {
    console.error("Error in analyze-emotion function:", error);
    const errorMessage = error instanceof Error ? error.message : "Failed to analyze emotion";
    return new Response(
      JSON.stringify({ error: errorMessage }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
